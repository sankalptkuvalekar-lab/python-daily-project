#sort() - ascending by default

list1 = [1,9,0,45,23,87,13,5,3]
list1.sort()
print(list1)


list2 = ["banana", "apple", "chikoo", "strwberry", "mango"]

list2.sort()
print(list2)

#sorting in descending order
#reverse= True

list1.sort(reverse=True)
print(list1)

list2.sort(reverse=True)
print(list2)

 
